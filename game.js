// 初始化变量
let availableNumbers = [];
let drawnNumbers = [];
let questionIndex = -1;

// 获取DOM元素
const questionDisplay = document.getElementById('questionDisplay');
const generateQuestionBtn = document.getElementById('generateQuestion');
const drawNumberBtn = document.getElementById('drawNumber');
const resetNumbersBtn = document.getElementById('resetNumbers');
const currentNumber = document.getElementById('currentNumber');
const remainingNumbers = document.getElementById('remainingNumbers');
const drawnList = document.getElementById('drawnList');

// 初始化数字池
function initNumbers() {
    availableNumbers = [];
    for (let i = 1; i <= 100; i++) {
        availableNumbers.push(i);
    }
}

// 打乱数组顺序
function shuffleArray(array) {
    const shuffled = [...array];
    for (let i = shuffled.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
    }
    return shuffled;
}

// 生成随机问题
function generateQuestion() {
    const shuffledQuestions = shuffleArray(questions);
    questionIndex = (questionIndex + 1) % shuffledQuestions.length;
    
    questionDisplay.style.animation = 'none';
    questionDisplay.offsetHeight;
    questionDisplay.style.animation = 'pulse 0.5s ease-in-out';
    
    questionDisplay.textContent = shuffledQuestions[questionIndex];
}

// 抽取数字
function drawNumber() {
    if (availableNumbers.length === 0) {
        currentNumber.textContent = '所有数字已抽取完毕！';
        currentNumber.style.animation = 'none';
        currentNumber.offsetHeight;
        currentNumber.style.animation = 'pulse 0.5s ease-in-out';
        return;
    }
    
    const randomIndex = Math.floor(Math.random() * availableNumbers.length);
    const number = availableNumbers[randomIndex];
    
    availableNumbers.splice(randomIndex, 1);
    drawnNumbers.push(number);
    
    currentNumber.textContent = `当前数字：${number}`;
    currentNumber.style.animation = 'none';
    currentNumber.offsetHeight;
    currentNumber.style.animation = 'pulse 0.5s ease-in-out';
    
    remainingNumbers.textContent = `剩余数字：${availableNumbers.length}个`;
    
    updateDrawnList();
}

// 更新已抽取数字列表
function updateDrawnList() {
    drawnList.innerHTML = '';
    const sortedDrawn = [...drawnNumbers].sort((a, b) => a - b);
    
    sortedDrawn.forEach(num => {
        const span = document.createElement('span');
        span.className = 'drawn-number';
        span.textContent = num;
        drawnList.appendChild(span);
    });
}

// 重置数字
function resetNumbers() {
    initNumbers();
    drawnNumbers = [];
    currentNumber.textContent = '当前数字：-';
    remainingNumbers.textContent = '剩余数字：100个';
    updateDrawnList();
}

// 事件监听
generateQuestionBtn.addEventListener('click', generateQuestion);
drawNumberBtn.addEventListener('click', drawNumber);
resetNumbersBtn.addEventListener('click', resetNumbers);

